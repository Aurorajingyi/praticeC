#include <stdio.h>
int main()
{
	int i,j;
	scanf("%d:%d",&i,&j);//24小时制 
	if(i==0){
		printf("12:%d AM",j); //如果24小时制是0：30，12小时制是12:30 AM 
	} else if(i==12){
		printf("%d:%d PM",i,j); //如果24小时制是12：30，12小时制是12:30 PM
	}else if(i>12){
		i=i-12;
		printf("%d:%d PM",i,j);
	}else{
		printf("%d:%d AM",i,j);
	}
	return 0;
}
